// Helper functions for backdrop management
export const getBackdropById = (id) => {
  return Object.values(luxuryBackdrops).find(backdrop => backdrop.id === id);
};

export const getBackdropsByCategory = (category) => {
  if (category === 'all') return Object.values(luxuryBackdrops);
  return Object.values(luxuryBackdrops).filter(backdrop => backdrop.category === category);
};

export const getFeaturedBackdrops = () => {
  return Object.values(luxuryBackdrops).filter(backdrop => backdrop.featured);
};

export const getPremiumBackdrops = () => {
  return Object.values(luxuryBackdrops).filter(backdrop => backdrop.premium);
};

export const getBackdropsByColor = (color) => {
  return Object.values(luxuryBackdrops).filter(backdrop => 
    backdrop.color.toLowerCase().includes(color.toLowerCase())
  );
};

export const getBackdropsByMood = (mood) => {
  return Object.values(luxuryBackdrops).filter(backdrop => 
    backdrop.mood.toLowerCase().includes(mood.toLowerCase())
  );
};

export const getBackdropsByPrice = (minPrice = 0, maxPrice = Infinity) => {
  return Object.values(luxuryBackdrops).filter(backdrop => 
    backdrop.price >= minPrice && backdrop.price <= maxPrice
  );
};

export const getAllBackdrops = () => {
  return Object.values(luxuryBackdrops);
};

export const getBackdropCategories = () => {
  return Object.values(backdropCategories);
};

export const getCategoryWithCount = () => {
  return Object.values(backdropCategories).map(category => ({
    ...category,
    count: category.id === 'all' 
      ? Object.values(luxuryBackdrops).length 
      : getBackdropsByCategory(category.id).length
  }));
};

// Backdrop pricing and availability
export const getBackdropPricing = (backdropId, duration = 4) => {
  const backdrop = getBackdropById(backdropId);
  if (!backdrop) return null;

  const basePrice = backdrop.price;
  const extendedHours = Math.max(0, duration - 4);
  const hourlyRate = 25; // Additional hourly rate
  
  return {
    basePrice,
    extendedHours,
    hourlyRate,
    total: basePrice + (extendedHours * hourlyRate)
  };
};

export const checkBackdropAvailability = (backdropId, date) => {
  const backdrop = getBackdropById(backdropId);
  if (!backdrop) return false;
  
  // In a real implementation, this would check against a booking database
  return backdrop.availability === 'In Stock';
};

